#include "libft.h"

int		ft_isspace(int c)
{
	unsigned char i;

	i = (unsigned char)c;
	if (i == ' ' || i == '\n' || i == '\t' ||
			i == '\v' || i == '\r' || i == '\f')
		return (1);
	return (0);
}
